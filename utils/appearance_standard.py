"""外貌提取强制标准 — 所有 Agent 共用"""
import re

# ═══════════════════════════════════════════
# 五维度模板
# ═══════════════════════════════════════════
APPEARANCE_STANDARD = """外貌必须包含五个维度，用逗号分隔，50-80字：
①体形：身高+胖瘦+身形特征
②脸型五官：脸型+眉+眼+鼻+唇+肤色+皮肤状态
③发型：长度+颜色+造型+打理状态
④穿着：上衣+下装+鞋(款式+颜色+材质)
⑤标志特征(可选)

严禁写入：叼烟/抽烟/瘫坐/倚靠/跷腿/叉腰/手插兜/敲键盘/拿手机(动作)
严禁写入：皱眉/微笑/冷峻/懒散/麻木/愤怒/委屈/漫不经心(表情)
严禁写入：盯着/看着/坐在/站在/走着(姿态)

正确示例：「中等身高偏清瘦，鹅蛋脸，淡眉杂乱眼皮微耷，唇薄，肤色苍白带黑眼圈。黑色短发凌乱微油。宽松旧白T恤配薄运动裤，光脚拖鞋。」
错误示例：「嘴里叼着烟，瘫在电竞椅上，眼神懒散地打游戏」← 全是动作表情，不合格"""

# ═══════════════════════════════════════════
# 正则清洗函数（终端兜底）
# ═══════════════════════════════════════════
_ACTION_WORDS = ['叼着', '叼烟', '抽烟', '吸烟', '瘫坐', '瘫在', '倚靠', '跷腿', '叉腰', '抱臂',
    '手插兜', '敲键盘', '拿手机', '拎包', '端着', '喝着', '吃着', '靠在', '坐在', '站在',
    '走到', '躺着', '蹲着', '趴在', '坐在电脑前', '推门', '推门进入', '抬手', '抹泪',
    '转身', '走出去', '走进来', '回过头', '伸手指着']
_EXPR_WORDS = ['皱眉', '微笑', '冷峻', '懒散', '麻木', '愤怒', '委屈', '紧张', '漫不经心',
    '敷衍', '随意', '盯着', '看着', '眯着', '半垂眼', '半低着头', '不看人', '一脸',
    '压着火', '压着怒气', '红了眼', '红着眼', '含着泪', '声音发颤']
_ENV_WORDS = ['卧室环境杂乱', '卧室杂乱', '房间杂乱', '环境杂乱', '背景是', '周围', '身旁',
    '踏入卧室', '进入卧室', '站在门口']

def clean_appearance(raw: str) -> str:
    """强制清洗外貌字段：移除所有动作/姿态/表情/环境词"""
    if not raw: return ''
    for w in _ACTION_WORDS:
        raw = re.sub(w + r'[^，,。.]{0,20}[，,。.]?', '', raw)
    for w in _EXPR_WORDS:
        raw = raw.replace(w, '')
    for w in _ENV_WORDS:
        raw = raw.replace(w, '')
    raw = re.sub(r'，+', '，', raw)
    raw = re.sub(r'^[，,]\s*', '', raw)
    raw = re.sub(r'[，,]\s*$', '', raw)
    result = raw.strip('，, ')
    # 清洗后如果只剩下无意义词，返回空触发LLM重新生成
    garbage = ['穿着', '穿着随意', '穿着普通', '穿着简单', '个子', '长得']
    if result in garbage or len(result) < 8:
        return ''
    return result
